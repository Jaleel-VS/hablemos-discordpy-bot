"""
Ask cog — owner-only Gemini Q&A with paginated responses.
"""
import asyncio
import logging

import discord
from discord.ext import commands
from google import genai
from google.genai import types

from base_cog import BaseCog

logger = logging.getLogger(__name__)

PAGE_CONTENT_LIMIT = 3900
GEMINI_TIMEOUT = 30


def split_response(text: str) -> list[str]:
    """Split text into pages that fit in embed descriptions."""
    if len(text) <= PAGE_CONTENT_LIMIT:
        return [text]

    pages = []
    remaining = text

    while remaining:
        if len(remaining) <= PAGE_CONTENT_LIMIT:
            pages.append(remaining)
            break

        chunk = remaining[:PAGE_CONTENT_LIMIT]

        split_idx = chunk.rfind('\n```')
        if split_idx > PAGE_CONTENT_LIMIT // 2:
            split_idx += 1
        elif (idx := chunk.rfind('\n\n')) > PAGE_CONTENT_LIMIT // 2 or (idx := chunk.rfind('\n')) > PAGE_CONTENT_LIMIT // 2:
            split_idx = idx
        else:
            split_idx = PAGE_CONTENT_LIMIT

        pages.append(remaining[:split_idx])
        remaining = remaining[split_idx:].lstrip('\n')

    return pages


class VisibilityView(discord.ui.View):
    """Buttons to choose public or private delivery after generation."""

    def __init__(self, pages: list[str], question: str, author_id: int):
        super().__init__(timeout=120)
        self.pages = pages
        self.question = question
        self.author_id = author_id

    def _build_embed(self, page_idx: int = 0) -> discord.Embed:
        title = self.question[:256]
        embed = discord.Embed(title=title, description=self.pages[page_idx], color=0x5865F2)
        footer = "Generated by AI"
        if len(self.pages) > 1:
            footer += f" — Page {page_idx + 1}/{len(self.pages)}"
        embed.set_footer(text=footer)
        return embed

    async def interaction_check(self, interaction: discord.Interaction) -> bool:
        return interaction.user.id == self.author_id

    @discord.ui.button(label='Send Public', style=discord.ButtonStyle.primary)
    async def send_public(self, interaction: discord.Interaction, button: discord.ui.Button):
        # Delete the prompt message with buttons
        await interaction.message.delete()

        if len(self.pages) == 1:
            await interaction.channel.send(embed=self._build_embed())
        else:
            view = PageView(self.pages, self.question, self.author_id)
            await interaction.channel.send(embed=view.build_embed(), view=view)

    @discord.ui.button(label='Send Private', style=discord.ButtonStyle.secondary)
    async def send_private(self, interaction: discord.Interaction, button: discord.ui.Button):
        # Respond with ephemeral first, then delete the prompt
        if len(self.pages) == 1:
            await interaction.response.send_message(embed=self._build_embed(), ephemeral=True)
        else:
            view = PageView(self.pages, self.question, self.author_id)
            await interaction.response.send_message(embed=view.build_embed(), view=view, ephemeral=True)

        await interaction.message.delete()

    @discord.ui.button(label='Discard', style=discord.ButtonStyle.danger)
    async def discard(self, interaction: discord.Interaction, button: discord.ui.Button):
        await interaction.message.delete()

    async def on_timeout(self):
        """Edit message when buttons expire."""
        try:
            # self.message is set after we send/edit with this view
            if self.message:
                await self.message.edit(content="Response expired (no choice made).", view=None)
        except discord.NotFound:
            pass


class PageView(discord.ui.View):
    """Pagination buttons for multi-page responses."""

    def __init__(self, pages: list[str], question: str, author_id: int):
        super().__init__(timeout=300)
        self.pages = pages
        self.question = question
        self.author_id = author_id
        self.current = 0

    def build_embed(self) -> discord.Embed:
        title = self.question[:256]
        embed = discord.Embed(title=title, description=self.pages[self.current], color=0x5865F2)
        footer = f"Generated by AI — Page {self.current + 1}/{len(self.pages)}"
        embed.set_footer(text=footer)
        return embed

    async def interaction_check(self, interaction: discord.Interaction) -> bool:
        return interaction.user.id == self.author_id

    @discord.ui.button(label='◀', style=discord.ButtonStyle.secondary)
    async def prev_page(self, interaction: discord.Interaction, button: discord.ui.Button):
        if self.current > 0:
            self.current -= 1
        await interaction.response.edit_message(embed=self.build_embed(), view=self)

    @discord.ui.button(label='▶', style=discord.ButtonStyle.secondary)
    async def next_page(self, interaction: discord.Interaction, button: discord.ui.Button):
        if self.current < len(self.pages) - 1:
            self.current += 1
        await interaction.response.edit_message(embed=self.build_embed(), view=self)


class AskCog(BaseCog):
    """Owner-only Gemini Q&A."""

    def __init__(self, bot: commands.Bot):
        super().__init__(bot)
        self.client = genai.Client(api_key=bot.settings.gemini_api_key)
        self.model_name = 'gemini-2.0-flash-lite'

    def _call_gemini(self, question: str):
        """Sync Gemini call — run in executor for timeout support."""
        return self.client.models.generate_content(
            model=self.model_name,
            contents=question,
            config=types.GenerateContentConfig(
                temperature=0.7,
                max_output_tokens=4096,
            ),
        )

    @commands.command(name='ask')
    @commands.is_owner()
    async def ask(self, ctx: commands.Context, *, question: str):
        """
        Ask Gemini anything. Owner only.

        Usage: $ask <question>
        """
        processing = await ctx.send("Thinking...")

        try:
            # Wrap sync Gemini call in executor with timeout
            loop = asyncio.get_running_loop()
            try:
                response = await asyncio.wait_for(
                    loop.run_in_executor(None, self._call_gemini, question),
                    timeout=GEMINI_TIMEOUT,
                )
            except TimeoutError:
                await processing.edit(content=f"Gemini didn't respond within {GEMINI_TIMEOUT}s. Try again.")
                return

            text = response.text
            if not text:
                await processing.edit(content="Gemini returned an empty response.")
                return

            pages = split_response(text)
            view = VisibilityView(pages, question, ctx.author.id)
            msg = await processing.edit(content="Ready. How do you want to send it?", view=view)
            view.message = msg or processing  # edit returns None in some versions

        except Exception as e:
            logger.error("Ask command error: %s", e, exc_info=True)
            error_str = str(e).lower()
            if any(k in error_str for k in ('safety', 'blocked')):
                await processing.edit(content="Response blocked by content policy.")
            elif any(k in error_str for k in ('rate', 'quota', '429')):
                await processing.edit(content="Rate limited. Try again in a minute.")
            else:
                await processing.edit(content=f"Error: {e}")


async def setup(bot: commands.Bot):
    await bot.add_cog(AskCog(bot))
